import { isNumber,isIntNumer,isEmail,isMobile,isPhone,isURL,checkIdCard } from "@/utils/validate";

export default {
  data() {
    return {
      ruleForm: {},
      flag: '',
      usersFlag: false,
      toubiaorenxingbieOptions: [],
      zhaobiaorenxingbieOptions: [],
      pingbiaorenxingbieOptions: [],
    };
  },
  mounted() {
    var table = this.$storage.get("sessionTable");
    this.flag = table;
    this.$http({
      url: `${this.$storage.get("sessionTable")}/session`,
      method: "get"
    }).then(({ data }) => {
      if (data && data.code === 0) {
        this.ruleForm = data.data;
      } else {
        this.$message.error(data.msg);
      }
    });
    this.toubiaorenxingbieOptions = "男,女".split(',')
    this.zhaobiaorenxingbieOptions = "男,女".split(',')
    this.pingbiaorenxingbieOptions = "男,女".split(',')
  },
  methods: {
    toubiaorentouxiangUploadChange(fileUrls) {
        this.ruleForm.touxiang = fileUrls;
    },
    zhaobiaorentouxiangUploadChange(fileUrls) {
        this.ruleForm.touxiang = fileUrls;
    },
    pingbiaorentouxiangUploadChange(fileUrls) {
        this.ruleForm.touxiang = fileUrls;
    },
    onUpdateHandler() {
      if((!this.ruleForm.yonghuming)&& 'toubiaoren'==this.flag){
        this.$message.error('用户名不能为空');
        return
      }
      if((!this.ruleForm.mima)&& 'toubiaoren'==this.flag){
        this.$message.error('密码不能为空');
        return
      }
      if( 'toubiaoren' ==this.flag && this.ruleForm.shenfenzheng&&(!checkIdCard(this.ruleForm.shenfenzheng))){
        this.$message.error(`身份证应输入身份证格式`);
        return
      }
      if( 'toubiaoren' ==this.flag && this.ruleForm.shouji&&(!isMobile(this.ruleForm.shouji))){
        this.$message.error(`手机应输入手机格式`);
        return
      }
      if((!this.ruleForm.zhaobiaozhanghao)&& 'zhaobiaoren'==this.flag){
        this.$message.error('招标账号不能为空');
        return
      }
      if((!this.ruleForm.mima)&& 'zhaobiaoren'==this.flag){
        this.$message.error('密码不能为空');
        return
      }
      if( 'zhaobiaoren' ==this.flag && this.ruleForm.dianhua&&(!isMobile(this.ruleForm.dianhua))){
        this.$message.error(`电话应输入手机格式`);
        return
      }
      if((!this.ruleForm.zhanghao)&& 'pingbiaoren'==this.flag){
        this.$message.error('账号不能为空');
        return
      }
      if((!this.ruleForm.mima)&& 'pingbiaoren'==this.flag){
        this.$message.error('密码不能为空');
        return
      }
      if( 'pingbiaoren' ==this.flag && this.ruleForm.shenfenzheng&&(!checkIdCard(this.ruleForm.shenfenzheng))){
        this.$message.error(`身份证应输入身份证格式`);
        return
      }
      if( 'pingbiaoren' ==this.flag && this.ruleForm.shouji&&(!isMobile(this.ruleForm.shouji))){
        this.$message.error(`手机应输入手机格式`);
        return
      }
      if('users'==this.flag && this.ruleForm.username.trim().length<1) {
	this.$message.error(`用户名不能为空`);
        return	
      }
      this.$http({
        url: `${this.$storage.get("sessionTable")}/update`,
        method: "post",
        data: this.ruleForm
      }).then(({ data }) => {
        if (data && data.code === 0) {
          this.$message({
            message: "修改信息成功",
            type: "success",
            duration: 1500,
            onClose: () => {
            }
          });
        } else {
          this.$message.error(data.msg);
        }
      });
    }
  }
};
